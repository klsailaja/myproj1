package com.ab.quiz.pojo;

public class TransferRequest {
	private int accType;
	private int amount;
	
	public int getAccType() {
		return accType;
	}
	public void setAccType(int accType) {
		this.accType = accType;
	}
	public int getAmount() {
		return amount;
	}
	public void setAmount(int amount) {
		this.amount = amount;
	}
}
